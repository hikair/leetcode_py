# 1093. 大样本统计
# https://leetcode.cn/problems/statistics-from-a-large-sample/
from typing import List


class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        total = sum(count)
        left, right = (total + 1) // 2, (total + 2) // 2
        min_v, max_v, median, mode, t, freq = 256, 0, 0, 0, 0, 0
        sum_v = 0
        for x, y in enumerate(count):
            sum_v += x * y
            if y > freq:
                freq, mode = y, x
            if y > 0:
                if min_v == 256:
                    min_v = x
                max_v = x
            if t < right <= (t + y):
                median += x
            if t < left <= (t + y):
                median += x
            t += y
        return [min_v, max_v, sum_v / total, median / 2, mode]


